    private void btnAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirActionPerformed

        Modelo_Usuario usuario = new Modelo_Usuario();
        Ctrl_Usuario ctrl_Usuario = new Ctrl_Usuario();

        if (verificarDatos()) {
            if (!ctrl_Usuario.existeUsuario(txtUsuario.getText().trim())) {
                String combo = "" + cbotipoU.getSelectedItem();
                if (!combo.equalsIgnoreCase("Seleccione un tipo de usuario:")) 
                {
                    usuario.setNombre(txtNombre.getText().trim());
                    usuario.setApellido(txtApellido.getText().trim());
                    usuario.setUsuario(txtUsuario.getText().trim());
            usuario.setTelefono(jPrefijos1.getNumero().trim());
                    usuario.setPassword(txtPassword.getText().trim());
                    usuario.setTipoU(combo);
                    System.out.println("El tipo de usuario seleccionado es");
                    System.out.println(combo);

                    if (ctrl_Usuario.guardarUsuario(usuario)) {
                        JOptionPane.showMessageDialog(null, "Registro Guardado");
                        limpiar();
                    }
                    else {
                        JOptionPane.showMessageDialog(null, "Error al guardar");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Selecciona un tipo de usuario");
                }
            } else {
                JOptionPane.showMessageDialog(null, "El usuario ya existe en la BD");

            }
        } 
    }//GEN-LAST:event_btnAñadirActionPerformed

