    //Metodo para llenar el comboBox
    //Metodo para asignar idCategoria
    private void limpiar() {
        txtApellido.setText(null);
        txtUsuario.setText(null);
        txtNombre.setText(null);
        txtPassword.setText(null);
      jPrefijos1.setNumero(null);
        cbotipoU.setSelectedIndex(0);
    }
    
    private boolean verificarDatos() {
        if (txtNombre.getText().equals("") || txtApellido.getText().equals("") || txtUsuario.getText().equals("") || jPrefijos1.getNumero().equals("") || txtPassword.getText().equals("")) {
             JOptionPane.showMessageDialog(null, "Rellene todos los campos");
            return false;
        } else {
            if (txtNombre.getText().matches("[a-zA-Z ]+") || txtApellido.getText().matches("[a-zA-Z ]+")) {
                return true;
            } else {
                JOptionPane.showMessageDialog(null, "Solo se aceptan letras en el nombre y apellido");
                return false;                
            }
        }
    }
