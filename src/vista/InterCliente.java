    private void btnAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirActionPerformed
        Modelo_Cliente cliente = new Modelo_Cliente();
        Ctrl_Cliente ctrl_Cliente = new Ctrl_Cliente();
        if (verificarDatos()) {
            if (ctrl_Cliente.existeCliente(txtCedula.getText())) {

                dialogoVerificar.setSize(560, 200);
                dialogoVerificar.setLocation(483, 199);
                dialogoVerificar.setVisible(true);
                lblCNombre.setText(txtNombre.getText());
                lblCApellido.setText(txtApellido.getText());
                lblCDireccion.setText(txtDireccion.getText());
                lblCTelefono.setText(jPrefijos1.getLada() + jPrefijos1.getNumero());
                lblCCorreo.setText(txtCorreo.getText());
                lblCCedula.setText(txtCedula.getText());
            } else {
                JOptionPane.showMessageDialog(null, "La cedula ya existe");
            }
        }
    }//GEN-LAST:event_btnAñadirActionPerformed

    private boolean verificarDatos() {
        if (txtNombre.getText().equals("") || txtApellido.getText().equals("") || txtDireccion.getText().equals("") || txtCorreo.getText().equals("") || txtCedula.getText().equals("")) {
           JOptionPane.showMessageDialog(null, " Rellene todos los campos");
            return false;
        } else {
            if (txtNombre.getText().matches("[a-zA-Z ]+") || txtApellido.getText().matches("[a-zA-Z ]+")) {
                return true;
            } else {
                JOptionPane.showMessageDialog(null, "Solo se aceptan letras en el nombre y apellido");
                return false;                
            }
        }
    }
    
