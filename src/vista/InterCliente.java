    private void btnAceptarEnvioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarEnvioActionPerformed
        dialogoVerificar.dispose();
        if (!txtCorreo.getText().isEmpty()) {
            Ctrl_Cliente ctrl_Cliente = new Ctrl_Cliente();
            if (!ctrl_Cliente.existeCorreo(txtCorreo.getText())) {
                Ctrl_Correo ctrlCorreo = new Ctrl_Correo();
                codig = "" + ctrlCorreo.sendEmail(txtCorreo.getText().trim());
                ConfirmarCorreo.setLocation(100, 100);
                ConfirmarCorreo.setSize(400, 130);
                ConfirmarCorreo.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "El correo ya esta en uso");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Ingresa el correo para validarlo");
        }
        //enviarCorreo();
        this.limpiar();
    }//GEN-LAST:event_btnAceptarEnvioActionPerformed

